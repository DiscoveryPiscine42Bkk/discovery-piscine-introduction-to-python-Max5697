#!/usr/bin/python3

Fisrt_num = int(input("Give me the first number: "))
sec_num = int(input("Give me the second number: "))
decimal_num = int(Fisrt_num / sec_num)

print("Thank you!")
print(f"{Fisrt_num} + {sec_num} =", Fisrt_num + sec_num)
print(f"{Fisrt_num} - {sec_num} =", Fisrt_num - sec_num)
print(f"{Fisrt_num} / {sec_num} =", decimal_num) 
print(f"{Fisrt_num} * {sec_num} =", Fisrt_num * sec_num)