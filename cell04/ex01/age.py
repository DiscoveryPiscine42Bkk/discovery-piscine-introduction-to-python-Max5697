#!/usr/bin/python3

age_text = int(input("Please tell me your age: "))

print(f"You are currently {age_text} years old.")

first_num = age_text + 10
sec_num = first_num + 10
thr_num = sec_num + 10

print(f"In 10 years, you'll be {first_num} years old.")
print(f"In 20 years, you'll be {sec_num} years old.")
print(f"In 30 years, you'll be {thr_num} years old.")